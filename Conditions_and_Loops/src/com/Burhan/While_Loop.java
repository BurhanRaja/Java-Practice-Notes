package com.Burhan;

public class While_Loop {
    public static void main(String[] args) {
        
        // Use while loop when you don't the total prints you have to make

        // To print 1 to 5 
        int num = 1;
        while (num<=5) {
            System.out.print(num + " ");
            num++;
        }


    }
}
